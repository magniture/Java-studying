/**
 * Initial state
 */
const state = {
  /**
   * 配置选项
   */
  options: {}
}

/**
 * Getters
 * @type {import('vuex/types').GetterTree<typeof state}
 */
const getters = {
  /**
   * 获取配置选项
   */
  options: state => state.options
}

/**
 * Mutations
 * @type {import('vuex/types').MutationTree<typeof state}
 */
const mutations = {
  /**
   * 设置配置选项
   */
  CHANGE_OPTIONS: (state, options) => {
    Object.assign(state.options, options)
  }
}

/**
 * Actions
 * @type {import('vuex/types').ActionTree<typeof state}
 */
const actions = {
  /**
   * 设置配置选项
   */
  changeOptions: async ({ commit }, options) => {
    commit('CHANGE_OPTIONS', options)
  }
}

// Export module
export default { state, getters, mutations, actions }
